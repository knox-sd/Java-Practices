
public class Java_expression {

	public static void main(String[] args) {
		// expression = operands & operators
		// operands = values, variables, numbers, quantity
		// operators = + - * / %
		
		int friends = 10;
		friends = friends + 1;
		friends++;
		
		friends = friends - 1;
		friends--;
		
		friends = friends * 2;
		friends = friends / 2;
		friends = friends % 2;
		System.out.println(friends);
		
		//for decimal value
		double friends = 10;
		friends = (double) friends / 3;
		System.out.println(friends);

	}

}
