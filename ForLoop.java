public class ForLoop {
    public static void main(String[] args){
        // for loop = Executes a block of code a limited amount of times
        // for (int i = 0; i <=10; i++){
        for (int i = 10; i >=0; i--){
            System.out.println(i);
        }
        System.out.println("Happy New Year!");
    }
}
